CREATE FUNCTION FUNC_CALCULA_IMPOSTO(@RENDA NUMERIC(10,2))
RETURNS NUMERIC(10,2) AS
	BEGIN
		DECLARE @PAGAMENTO NUMERIC(10,2)
		
		IF (@RENDA < 1372.82) 
			BEGIN
				SET @PAGAMENTO = 0
			END
		ELSE IF ((@RENDA >= 1372.82) AND (@RENDA <= 2743.25))
			BEGIN
				SET @PAGAMENTO = (@RENDA * 0.15) - 205.92
			END
		ELSE
			BEGIN
				SET @PAGAMENTO = (@RENDA * 0.275) - 548.82
			END
		RETURN @PAGAMENTO
	END

DECLARE @IMPOSTO NUMERIC(10,2)
EXEC @IMPOSTO = FUNC_CALCULA_IMPOSTO 3580
