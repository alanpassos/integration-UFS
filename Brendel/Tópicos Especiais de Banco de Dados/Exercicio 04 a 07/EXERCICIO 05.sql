CREATE PROCEDURE SP_IMPOSTO_RENDA(@RENDA NUMERIC(10,2), @IMPOSTO NUMERIC(10,2) OUTPUT) AS
IF (@RENDA < 1372.82)
	BEGIN
		SET @IMPOSTO = 0
	END
ELSE IF (@RENDA >= 1372.82 AND @RENDA <= 2743.25)
	BEGIN
		SET @IMPOSTO = @RENDA * 0.15
	END
ELSE IF (@RENDA > 2743.25)
	BEGIN
		SET @IMPOSTO = @RENDA * 0.275
	END
SELECT @IMPOSTO