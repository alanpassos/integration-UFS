CREATE FUNCTION SP_IMPOSTO (@RENDA NUMERIC(10,2))
RETURNS NUMERIC(10,2)
AS
BEGIN
	DECLARE @IMPOSTO NUMERIC(10,2)
	IF (@RENDA < 1372.82)
		BEGIN
			SET @IMPOSTO = 0
		END
	ELSE IF (@RENDA >= 1372.82 AND @RENDA <= 2743.25)
		BEGIN
			SET @IMPOSTO = @RENDA * 0.15
			SET @IMPOSTO = @IMPOSTO - 205.92
		END
	ELSE IF (@RENDA > 2743.25)
		BEGIN
			SET @IMPOSTO = @RENDA * 0.275
			SET @IMPOSTO = @IMPOSTO - 548.82 	
		END
	RETURN @IMPOSTO
END
