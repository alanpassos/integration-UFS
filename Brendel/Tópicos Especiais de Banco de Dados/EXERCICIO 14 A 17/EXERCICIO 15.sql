
CREATE FUNCTION SF_DATACOMPLETA (@DATA DATETIME)
RETURNS VARCHAR(40)
AS
BEGIN
	DECLARE @DATA_COMPLETA VARCHAR(40)
	SET @DATA_COMPLETA = DATENAME (DD, @DATA) + ' de ' + DATENAME (MM, @DATA)+ ' de '+DATENAME (YY, @DATA)
	RETURN @DATA_COMPLETA
END

SET LANGUAGE ENGLISH
DECLARE @DATA_COMPLETA VARCHAR(40)
EXEC @DATA_COMPLETA = SF_DATACOMPLETA '20130222'
SELECT @DATA_COMPLETA

