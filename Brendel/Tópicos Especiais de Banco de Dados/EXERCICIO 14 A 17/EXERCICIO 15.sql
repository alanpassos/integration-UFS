CREATE FUNCTION SF_LPAD(@STRING VARCHAR(8000), @TAMANHO INT, @CARACTER CHAR(1))
RETURNS VARCHAR(8000)
AS
BEGIN
	DECLARE @RETORNO VARCHAR(8000)
	SET @RETORNO = LEFT(@STRING, @TAMANHO)
	SET @RETORNO = REPLICATE(@CARACTER, @TAMANHO-LEN(@RETORNO))+@RETORNO
	RETURN @RETORNO
END

DECLARE @RETORNO VARCHAR(40)
EXEC @RETORNO = SF_LPAD 'tech on the net', 16, 'z'
SELECT @RETORNO
